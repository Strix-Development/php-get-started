<?php




// Operator	       Name        	         Description	
// ++$x 	       Pre-increment	     Increments $x by one, then returns $x	
// $x++	           Post-increment	     Returns $x, then increments $x by one	
// --$x 	       Pre-decrement	     Decrements $x by one, then returns $x	
// $x-- 	       Post-decrement	     Returns $x, then decrements $x by one








//Pre-increment ++$x.................................


$x=20;
echo ++$x . "<br>";



$y=50;
echo ++$y . "<br>";



$a=100;
echo ++$a ."<br>";



$b=70;
echo ++$b ."<br>";



$c=33;
echo ++$c ."<br>";


$d=59;
echo ++$d ."<br>";






//Post-increment $x++.............................



$a=50;
echo $a++ ."<br>";



$b=80;
echo $b++ ."<br>";



$c=70;
echo $c++ ."<br>";



$d=90;
echo $d++ ."<br>";



$e=2;
echo $e++ ."<br>";






//Pre-decrement --$x.........................




$a=40;
echo --$a ."<br>";



$b=10;
echo --$b ."<br>";



$c=78;
echo --$c ."<br>";



$d=98;
echo --$d ."<br>";



$e=3205;
echo --$e ."<br>";







//Post-decrement $x-- ....................................	      



$a=406;
echo $a-- ."<br>";



$b=1010;
echo $b-- ."<br>";



$c=78;
echo $c-- ."<br>";



$d=45;
echo $d-- ."<br>";



$e=93;
echo $e-- ."<br>";









?>