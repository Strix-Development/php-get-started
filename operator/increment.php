<?php

//INCREMENT AND DECREMENT
//pre increment operator
$ab = 10;
echo ++$ab; // output is 11
echo "<br>";
echo $ab; //out put is 11
echo "<br>";

//post increment operator
$cd = 10;
echo $cd++;  //out put is 10
echo "<br>";
echo $cd;  // out put is 11
echo "<br>";


//pre decrement operator
$ef = 20;
echo --$ef;
echo "<br>";
echo $ef;
echo "<br>";


//post decrement operator
$gh = 30;
echo $gh--;
echo "<br>";
echo $gh;
echo "<br>";


require_once __DIR__."/logical.php";

?>